package com.gonggu.copy.model.dto;


public class CopyDto {

	private String copyName;
	private int copyNo;
	private String copyPhoto;
	
	private String copyContent;
	private String copyAddress;
	private String copyNumber;
	
	
	public String getCopyContent() {
		return copyContent;
	}
	public void setCopyContent(String copyContent) {
		this.copyContent = copyContent;
	}
	public String getCopyAddress() {
		return copyAddress;
	}
	public void setCopyAddress(String copyAddress) {
		this.copyAddress = copyAddress;
	}
	public String getCopyNumber() {
		return copyNumber;
	}
	public void setCopyNumber(String copyNumber) {
		this.copyNumber = copyNumber;
	}
	public String getCopyName() {
		return copyName;
	}
	public void setCopyName(String copyName) {
		this.copyName = copyName;
	}
	public int getCopyNo() {
		return copyNo;
	}
	public void setCopyNo(int copyNo) {
		this.copyNo = copyNo;
	}
	public String getCopyPhoto() {
		return copyPhoto;
	}
	public void setCopyPhoto(String copyPhoto) {
		this.copyPhoto = copyPhoto;
	}
	
	
}
